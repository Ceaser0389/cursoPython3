"""
Fatiamento de Strings
012345678
Olá mundo
Fatiamento [ i:f:p] [::]
Obs: a função len retorna a qtd
de caracteres da str
"""

variavel = 'Olá Mundo'

print(variavel[4])
print('**********************')

print(variavel[-4])
print('**********************')

print(variavel[4:])
print('**********************')

print(variavel[1:4])
print('**********************')

print(len(variavel))   # tamanho da string
print('**********************')

print(variavel[0:9:1])
print('**********************')

print(variavel[-1:-10:-1])
print('**********************')

print(variavel[::-1])
